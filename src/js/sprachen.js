// Hilfsfunktionen für Sprachverwaltung
export function getLang() {
  const params = new URLSearchParams(window.location.search);
  return params.get('lang') || 'de';
}

export function setAGB(lang) {
  const content = document.getElementById('agb-content');
  if (content) {
    content.innerHTML = agbs[lang] || agbs['de'];
  }
  const select = document.getElementById('lang-switch');
  if (select) {
    select.value = lang;
  }
}

// Event Listener für Sprachwechsel
export function initLanguageSwitch() {
  document.addEventListener('DOMContentLoaded', function() {
    let lang = getLang();
    setAGB(lang);
    
    const select = document.getElementById('lang-switch');
    if (select) {
      select.addEventListener('change', function(e) {
        setAGB(e.target.value);
        // URL-Parameter aktualisieren
        const url = new URL(window.location);
        url.searchParams.set('lang', e.target.value);
        window.history.replaceState({}, '', url);
      });
    }
  });
} 